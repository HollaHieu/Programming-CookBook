# Here's official installation process: [Create a New React App](https://reactjs.org/docs/create-a-new-react-app.html), 
# and officially recommended [tutorials](https://reactjs.org/community/courses.html)

# install react cli
npm install -g create-react-app

# create app
create-react-app my-react-app-name

# go to project folder
cd my-react-app-name

# install dependencies
npm install

# start live server
npm start